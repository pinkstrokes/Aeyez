def safe_string_to_dict(s: str) -> dict:
    """Safely convert string to dictionary (from qwen7b script)."""
    if isinstance(s, dict):
        return s

    if not isinstance(s, str):
        return {}

    # Try multiple parsing methods
    try:
        # Method 1: JSON parsing
        return json.loads(s)
    except:
        try:
            # Method 2: ast.literal_eval
            import ast
            result = ast.literal_eval(s)
            if isinstance(result, dict):
                return result
        except:
            try:
                # Method 3: Handle single quotes to double quotes
                s_fixed = s.replace("'", '"')
                return json.loads(s_fixed)
            except:
                try:
                    # Method 4: Use eval (unsafe, but as last resort)
                    result = eval(s)
                    if isinstance(result, dict):
                        return result
                except:
                    pass

    return {}


def get_evaluation_score(pred: str, answers: List[str], question_type: str,
                        eval_method: str = None, question: str = "") -> float:
    """Get evaluation score based on question type using official OCRBench metrics."""

    if not OCRBENCH_EVAL_AVAILABLE:
        # Fallback to basic string comparison
        if not pred or not answers:
            return 0.0
        pred_clean = pred.strip().lower()
        for ans in answers:
            if ans.strip().lower() in pred_clean:
                return 1.0
