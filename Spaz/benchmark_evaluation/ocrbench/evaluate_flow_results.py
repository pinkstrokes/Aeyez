        if question_type in ["APP agent en", "ASCII art classification en", "math QA en",
                           "reasoning VQA en", "science QA en", "text recognition en",
                           "document classification en", "cognition VQA en", "diagram QA en"]:
            if eval_method == "multiple choice":
                if not isinstance(pred, str):
                    return 0.0
                pred_clean = ''.join(c for c in pred if c.isalpha())
                return 1.0 if pred_clean == answers[0] else 0.0
            elif eval_method == "case sensitive":
                return vqa_evaluation_case_sensitive(pred, answers)
            else:
                return vqa_evaluation(pred, answers)

